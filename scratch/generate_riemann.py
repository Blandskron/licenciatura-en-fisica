# --- CELL 8: FUNDAMENTAL THEOREMS ---
add_markdown([
    "## 4. Teoremas Fundamentales de la Integral de Riemann",
    "",
    "### 4.1 Teorema del Valor Medio para Integrales",
    "Si $f$ es una función continua en el intervalo cerrado $[a, b]$, entonces existe un número $c$ en el intervalo abierto $(a, b)$ tal que:",
    "$$f(c) = \\frac{1}{b-a} \\int_a^b f(x) dx$$",
    "El valor $f(c)$ se denomina **valor medio** de la función $f$ en el intervalo $[a, b]$. Geométricamente, esto significa que existe un rectángulo con base $b-a$ y altura $f(c)$ cuya área es exactamente igual al área bajo la curva de $f$ de $a$ a $b$.",
    "",
    "### 4.2 Primer Teorema Fundamental del Cálculo (FTC 1)",
    "Sea $f$ una función continua en $[a, b]$. Si definimos la **función de acumulación** $g(x)$ como:",
    "$$g(x) = \\int_a^x f(t) dt \\quad \\forall x \\in [a, b]$$",
    "entonces $g(x)$ es continua en $[a, b]$, derivable en $(a, b)$, y su derivada es:",
    "$$g'(x) = f(x)$$",
    "Es decir, la derivación es la operación inversa de la integración. En términos generales, usando la regla de la cadena, si los límites de integración dependen de funciones $u(x)$ y $v(x)$, tenemos la **Regla de Leibniz**:",
    "$$\\frac{d}{dx} \\left[ \\int_{u(x)}^{v(x)} f(t) dt \\right] = f(v(x)) v'(x) - f(u(x)) u'(x)$$",
    "",
    "### 4.3 Segundo Teorema Fundamental del Cálculo (Regla de Barrow)",
    "Si $f$ es continua en $[a, b]$ y $F$ es cualquier antiderivada de $f$ en $[a, b]$ (de modo que $F' = f$), entonces:",
    "$$\\int_a^b f(x) dx = F(b) - F(a)$$"
])

