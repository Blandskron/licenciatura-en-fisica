    "",
    "El **criterio clásico de integrabilidad** establece que una función $f$ acotada en $[a, b]$ es integrable si y solo si el conjunto de sus puntos de discontinuidad tiene **medida cero** (Teorema de Lebesgue). En términos prácticos para Cálculo II, esto significa que:",
