add_markdown([
    "## 1. Definición y Conceptos Básicos",
    "",
    "### 1.1 Partición de un Intervalo y Nodos",
    "Sea $[a, b]$ un intervalo cerrado y acotado en los números reales $\\mathbb{R}$. Una **partición** $P$ de $[a, b]$ es un conjunto finito de puntos ordenados:",
    "$$P = \\{x_0, x_1, x_2, \\dots, x_{n-1}, x_n\\}$$",
    "tales que:",
    "$$a = x_0 < x_1 < x_2 < \\dots < x_{n-1} < x_n = b$$",
    "Los puntos $x_i$ se denominan **nodos** de la partición. Estos nodos dividen a $[a, b]$ en $n$ subintervalos cerrados de la forma $[x_{i-1}, x_i]$. La longitud de cada subintervalo se denota por $\\Delta x_i = x_i - x_{i-1}$ para $i = 1, 2, \\dots, n$.",
    "",
    "Definimos la **norma de la partición** $P$, denotada por $\\|P\\|$, como la longitud del subintervalo más grande:",
    "$$\\|P\\| = \\max_{1 \\le i \\le n} \\Delta x_i$$",
    "Una partición se dice **regular** si todos los subintervalos tienen la misma longitud, en cuyo caso $\\Delta x_i = \\Delta x = \\frac{b-a}{n}$ para todo $i$.",
    "",
    "### 1.2 Sumas de Riemann",
    "Para una función acotada $f: [a, b] \\to \\mathbb{R}$, y dada una partición $P$, elegimos en cada subintervalo $[x_{i-1}, x_i]$ un **punto muestra** $c_i \\in [x_{i-1}, x_i]$. La **suma de Riemann** de $f$ asociada a la partición $P$ y a la elección de puntos muestra $\\{c_i\\}$ se define como:",
    "$$S(f, P, \\{c_i\\}) = \\sum_{i=1}^n f(c_i) \\Delta x_i$$",
    "Las tres elecciones comunes para los puntos muestra son:",
    "- **Suma por la izquierda (Left Riemann Sum):** $c_i = x_{i-1}$",
    "- **Suma por la derecha (Right Riemann Sum):** $c_i = x_i$",
    "- **Suma del punto medio (Midpoint Riemann Sum):** $c_i = \\frac{x_{i-1} + x_i}{2}$",
    "",
    "### 1.3 Límite e Integrabilidad de Riemann",
    "Se dice que la función $f$ es **integrable según Riemann** en $[a, b]$ si existe un número real $I$ tal que, para todo $\\epsilon > 0$, existe un $\\delta > 0$ tal que si $P$ es cualquier partición de $[a, b]$ con $\\|P\\| < \\delta$, entonces:",
    "$$\\left| \\sum_{i=1}^n f(c_i) \\Delta x_i - I \\right| < \\epsilon$$",
    "independientemente de la elección de los puntos muestra $c_i$. En este caso, escribimos:",
    "$$I = \\int_a^b f(x) dx = \\lim_{\\|P\\| \\to 0} \\sum_{i=1}^n f(c_i) \\Delta x_i$$"
])

